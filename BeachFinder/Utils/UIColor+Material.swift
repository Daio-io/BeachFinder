import UIKit

extension UIColor {
    
    static func Blue600() -> UIColor {
        return UIColor(colorLiteralRed: 0.118, green: 0.533, blue: 0.898, alpha: 1)
    }
    
    static func Amber500() -> UIColor {
        return UIColor(colorLiteralRed: 1, green: 0.757, blue: 0.027, alpha: 1)
    }
    
    static func BlueGrey50() -> UIColor {
        return UIColor(red:0.93, green:0.94, blue:0.95, alpha:1)
    }
}
