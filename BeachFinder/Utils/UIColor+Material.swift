import UIKit

extension UIColor {
    
    static var Blue600: UIColor {
        get { return UIColor(colorLiteralRed: 0.118, green: 0.533, blue: 0.898, alpha: 1) }
    }
    
    static var Amber500: UIColor {
        get { return UIColor(colorLiteralRed: 1, green: 0.757, blue: 0.027, alpha: 1) }
    }
    
    static var BlueGrey50: UIColor {
        get { return UIColor(red:0.93, green:0.94, blue:0.95, alpha:1) }
    }
}
